#include <string.h>

void	*ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*b2;

	b2 = (char*)s;
	i = 0;
	while (i < n)
	{
		b2[i] = '\0';
		i++;
	}
	return ((void*)b2);
}
